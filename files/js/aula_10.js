// 10° - Operadores aritimeticos.

// Anotações
// Somar = +
// Subtrair = -
// Multiplicação = *
// Divisão = /
// Raiz Quadra = **

// Imcremento = ++
// Decremento = --
// so mostra o valor Incre/Decre depois da função executada.

let salario = 5;
let salarioDecre = salario--;
let salarioIncre = salario++;

console.log(salario + salario);
console.log(salario - salario);
console.log(salario * salario);
console.log(salario / salario);
console.log(salario ** salario);
console.log(salarioIncre);
console.log(salarioDecre);